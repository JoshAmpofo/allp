/**
 * add - sum two numbers
 * @a: first number
 * @b: second number
 *
 * Return: sum of numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: first number
 * @b: second number
 *
 * Result: value of subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: first number
 * @b: second number
 *
 * Return: value of multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two numbers
 * @a: first number
 * @b: second number
 *
 * Result - value of division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - find the remainder of a division of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: remainder of division
 */
int mod(int a, int b)
{
	return (a % b);
}
